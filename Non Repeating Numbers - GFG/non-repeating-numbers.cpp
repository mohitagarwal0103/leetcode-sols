class Solution
{
public:
    vector<int> singleNumber(vector<int> v) 
    {
        sort(v.begin(),v.end());
        vector<int>ans;
        for (int i=0;i<v.size();i++)
        {
            if (v[i]!=v[i+1])
            ans.push_back(v[i]);
            else
            i++;
        }
        return ans;
        // Code here.
    }
};


